package io.sqm.core;

/**
 * A set of operators used in {@link ComparisonPredicate}.
 */
public enum ComparisonOperator {
    /**
     * Equal
     */
    EQ,
    /**
     * Not equal
     */
    NE,
    /**
     * Less than
     */
    LT,
    /**
     * Less than equal
     */
    LTE,
    /**
     * Greater than
     */
    GT,
    /**
     * Greater than equal
     */
    GTE
}
